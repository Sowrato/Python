from math import sqrt

print('\n','-'*5,'DESAFIO 006','-'*5,'\n')

valor = int(input('Digite um valor: '))

dobro = valor * 2
triplo = valor * 3
raiz_quadrada = sqrt(valor)

print(f'\nO dobro é {dobro}')
print(f'\nO triplo é {triplo}')
print(f'\nA raiz quadrada é {raiz_quadrada:.2f}')
print('\n','-'*5,'FIM DO PROGRAMA','-'*5,'\n')